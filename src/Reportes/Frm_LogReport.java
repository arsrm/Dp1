    private void btn_GenerarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GenerarReporteActionPerformed
        Calendar dateIniCal = Calendar.getInstance();
        Calendar dateFinCal = Calendar.getInstance();
        Date dateIniSearch = null;
        Date dateEndSearch = null;

        Prueba reporte = new Prueba();
        
        if (dateIni.getDate() != null) {
//            dateIniSearch = dateIni.getDate();
            dateIniCal.setTime(dateIni.getDate());
        } else {
//            dateIniSearch = new Date();
//            dateIniSearch.setTime(0);
            dateIniCal.setTime(new Date(0));
        }
        if (dateFin.getDate() != null) {
//            dateEndSearch = dateFin.getDate();
            dateFinCal.setTime(dateFin.getDate());
        } else {
//            dateEndSearch = new Date();
            dateFinCal.setTime(new Date());
        }
        if (dateFinCal.before(dateIniCal)) {
                JOptionPane.showMessageDialog(this, "La Fecha Fin debe ser mayor que la Fecha Inicio");
        }
        else{
            reporte.mostrarReporteLog(formatDate.format(dateIniCal.getTime()),formatDate.format(dateFinCal.getTime()));
        }
    }//GEN-LAST:event_btn_GenerarReporteActionPerformed

