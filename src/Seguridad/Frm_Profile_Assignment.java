    public void initializeTable(){
        int hasWindow;
        String status= null;
        List<ProfileWindow> profileWindowList = new ArrayList<>();
        
        hasWindow = daoProfile.profileHasWindows(profile.getIdprofile());
        if(modelo!=null){
            modelo.getDataVector().removeAllElements();
            modelo.fireTableDataChanged();
        }
        if(hasWindow==0){
            profileWindowList = daoProfile.windowsGet(profile.getIdprofile());
            for(int i=0; i<profileWindowList.size(); i++)
                daoProfile.profileWindowIns(profileWindowList.get(i));
        }else{
            profileWindowList = daoProfile.windowsGetById(profile.getIdprofile());
        }
        try {
            for (int i = 0; i < profileWindowList.size(); i++) {
                
                if (profileWindowList.get(i).getStatus()==0) status = "Inactivo";
                else status = "Activo";

                Object newRow[] = {
                    profileWindowList.get(i).getId_menu(),
                    profileWindowList.get(i).getId_windows(),
                    profileWindowList.get(i).getDescription(),
                    status,
                    false
                };
                modelo.addRow(newRow);
            }
        } catch (Exception e) {
        }
    }
    
