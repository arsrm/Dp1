    private void btn_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SaveActionPerformed
        Object[] options = {"OK"};
        int ok_option;
        if (directoryClient == null && directoryDriver == null && directoryTrademark == null) {
            ok_option = JOptionPane.showOptionDialog(new JFrame(), "Seleccione un archivo", "Mensaje", JOptionPane.PLAIN_MESSAGE, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
        } else {
            if (JOptionPane.showConfirmDialog(new JFrame(), "¬øDesea realizar acci√≥n?",
                    "Advertencias", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                if (directoryClient != null) {
                    loadClient();
                }
                if (directoryDriver != null) {
                    loadDriver();
                }
                if (directoryTrademark != null){
                    loadTrademark();
                }
                
                    ok_option = JOptionPane.showOptionDialog(new JFrame(), "Se ha cargado los archivos con √©xito", "Mensaje", JOptionPane.PLAIN_MESSAGE, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
                }
            }
        
    }//GEN-LAST:event_btn_SaveActionPerformed

