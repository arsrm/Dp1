    public void loadClient() {
        String line = null;

        File file = new File(directoryClient);
        BufferedReader reader = null;

        try {
            reader = new BufferedReader(new FileReader(file));
            reader.readLine();

            while ((line = reader.readLine()) != null) {
                Client client = new Client();
                String[] lineArray = line.split(",");
//                        client.setIdClient(Integer.parseInt(lineArray[0]));
                client.setRuc(lineArray[1]);
                client.setName(lineArray[2]);
                client.setAddress(lineArray[3]);
                client.setPriority(Integer.parseInt(lineArray[4]));
                client.setPos_x(Integer.parseInt(lineArray[5]));
                client.setPos_y(Integer.parseInt(lineArray[6]));
                client.setStatus(Integer.parseInt(lineArray[7]));
                daoClient.clientIns(client);
            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            if (reader != null) {
                try {
                    reader.close();
                } catch (IOException ex) {
                    
                }
            }
        }
    }

    public void loadDriver() {
        String line = null;
        Integer maxId = daoDriver.driverGetMaxId();
        File file = new File(directoryDriver);
        BufferedReader reader = null;

        try {
            reader = new BufferedReader(new FileReader(file));
            reader.readLine();

            while ((line = reader.readLine()) != null) {
                Driver driver = new Driver();
                String[] lineArray = line.split(",");
                driver.setIdDriver(maxId);
                driver.setName(lineArray[1]);
                driver.setStatus(Integer.parseInt(lineArray[2]));
                daoDriver.driverIns(driver);
                maxId++;
            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            if (reader != null) {
                try {
                    reader.close();
                } catch (IOException ex) {
                    
                }
            }
        }
    }
    
    public void loadTrademark() {
        String line = null;
        Integer maxId = daoTrademark.trademarkGetMaxId();
        File file = new File(directoryTrademark);
        BufferedReader reader = null;

        try {
            reader = new BufferedReader(new FileReader(file));
            reader.readLine();

            while ((line = reader.readLine()) != null) {
                Trademark trademark = new Trademark();
                String[] lineArray = line.split(",");
                trademark.setId_Trademark(maxId);
                trademark.setName(lineArray[1]);
                daoTrademark.trademarkIns(trademark);
                maxId++;
            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            if (reader != null) {
                try {
                    reader.close();
                } catch (IOException ex) {
                    
                }
            }
        }
    }
    
