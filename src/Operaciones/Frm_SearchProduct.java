    private void tbl_productsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_productsMouseClicked
        Product prod = null;
        if (evt.getSource() == tbl_products) {
            int rowSel = tbl_products.getSelectedRow();
            menuPadre.setTxtCodProduct(tbl_products.getValueAt(rowSel, 0).toString());
            menuPadre.setTxtEAN13(tbl_products.getValueAt(rowSel, 1).toString());
            menuPadre.setTxtName(tbl_products.getValueAt(rowSel, 2).toString());
            menuPadre.setTxtTrademark(tbl_products.getValueAt(rowSel, 3).toString());
            menuPadre.setEnabled(true);
            this.dispose();
        }
    }//GEN-LAST:event_tbl_productsMouseClicked

    public void initializeTable() {
        modelo.getDataVector().removeAllElements();
        modelo.fireTableDataChanged();
        List<Product> productList = daoProducts.ProductsQry();
        try {
            for (int i = 0; i < productList.size(); i++) {
                String trademark = daoTrademark.TrademarkGetById(productList.get(i).getTrademark()).getName();
                Object[] fila = {productList.get(i).getIdProduct(), productList.get(i).getCodeEAN13(),
                    productList.get(i).getName(), trademark};
                modelo.addRow(fila);
            }
        } catch (Exception e) {
        }
    }

