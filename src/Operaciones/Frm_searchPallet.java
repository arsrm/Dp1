    private void initializeTable() {
        modelo.getDataVector().removeAllElements();
        modelo.fireTableDataChanged();
        List<PalletIni> palletList = daoPallet.getFreePallet();
        try {
            for (int i = 0; i < palletList.size(); i++) {
                Object[] fila = {palletList.get(i).getIdpallet(), palletList.get(i).getDescription()};
                modelo.addRow(fila);
            }
        } catch (Exception e) {
        }
    }


